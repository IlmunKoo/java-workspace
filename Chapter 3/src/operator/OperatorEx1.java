package operator;

public class OperatorEx1 {

	public static void main(String[] args) {

		int num = 10%3;
		System.out.println(num);
		//;(세미콜론) 한글에서 마침표 찍듯 자바에서는 세미콜론으로
		//마침표 찍어 줘야 함, statement(문장) 끝났다는 의미
		
		int num1 = 10;
		num1 += 2;
		// 두 항의 값을 더해 왼쪽 항에 대입(10 + 2) = 12
		System.out.println(num1);
		
		 
		
		
	}

}
